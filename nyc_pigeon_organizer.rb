def nyc_pigeon_organizer(data)
  # write your code here!
  pigeon_list = {}

  data.each do |outer, inner|
  	inner.each do |adj, array|
  		array.each do |name|
  			if pigeon_list[name] == nil
  				pigeon_list[name] = {}
  			end

  			if pigeon_list[name][outer] == nil
  				pigeon_list[name][outer] = []
  			end

  			if !pigeon_list[name][outer].include?(adj)
  				pigeon_list[name][outer] << adj.to_s
  			end
  		end
  	end
  end
  pigeon_list
end

